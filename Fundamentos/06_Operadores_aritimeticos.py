produto_1 = 10
produto_2 = 20

# soma
print(produto_1 + produto_2)

# subtração
print(produto_1 - produto_2)

# multiplicação
print(produto_1 * produto_2)

# divisão normal
print(produto_1 / produto_2)
# divisão inteira
print(produto_1 // produto_2)

# potência
print(produto_1 ** 10)

# resto da divisão
print(produto_1 % produto_2)

# em expressões seguir a regra matemática de pesos
# parenteses, potências, multi e div (da esquerda -> direita) e som e sub (esquera -> direita)
x =  (10 + 15) * 4**3 - 25
print(x)